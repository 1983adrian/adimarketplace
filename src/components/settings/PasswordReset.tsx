import React, { forwardRef, useState } from 'react';
import { Lock, Shield, ExternalLink, Mail, Loader2 } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Dialog, DialogContent, DialogDescription, DialogHeader, DialogTitle, DialogTrigger } from '@/components/ui/dialog';
import { supabase } from '@/integrations/supabase/client';
import { useToast } from '@/hooks/use-toast';

interface PasswordResetProps {
  userEmail: string;
  authProvider?: string;
}

// Forward ref wrapper for dialog trigger content
const PasswordInfoCard = forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement> & { provider?: string }>(
  ({ provider, ...props }, ref) => (
    <div
      ref={ref}
      className="flex items-center justify-between p-4 rounded-lg border cursor-pointer hover:bg-muted/50 transition-colors"
      {...props}
    >
      <div>
        <p className="font-medium">Securitate Cont</p>
        <p className="text-sm text-muted-foreground">
          {provider === 'google' ? 'Gestionat prin Google OAuth' : 'SchimbƒÉ parola contului'}
        </p>
      </div>
      <Button variant="outline" size="sm" tabIndex={-1}>
        {provider === 'google' ? <Shield className="h-4 w-4" /> : <Lock className="h-4 w-4" />}
      </Button>
    </div>
  )
);
PasswordInfoCard.displayName = 'PasswordInfoCard';

export const PasswordReset: React.FC<PasswordResetProps> = ({ userEmail, authProvider }) => {
  const [isLoading, setIsLoading] = useState(false);
  const [emailSent, setEmailSent] = useState(false);
  const { toast } = useToast();

  const handlePasswordReset = async () => {
    setIsLoading(true);
    try {
      const { error } = await supabase.functions.invoke('send-password-reset', {
        body: { email: userEmail },
      });

      if (error) throw error;

      setEmailSent(true);
      toast({
        title: 'Email trimis!',
        description: 'VerificƒÉ inbox-ul pentru linkul de resetare a parolei.',
      });
    } catch (error: any) {
      console.error('Password reset error:', error);
      toast({
        title: 'Email trimis!',
        description: 'DacƒÉ existƒÉ un cont cu acest email, vei primi linkul de resetare.',
      });
      setEmailSent(true);
    } finally {
      setIsLoading(false);
    }
  };

  // For Google OAuth users
  if (authProvider === 'google') {
    return (
      <div className="space-y-4">
        <Dialog>
          <DialogTrigger asChild>
            <PasswordInfoCard provider="google" />
          </DialogTrigger>
          <DialogContent>
            <DialogHeader>
              <DialogTitle className="flex items-center gap-2">
                <Shield className="h-5 w-5 text-emerald-500" />
                Securitate Cont OAuth
              </DialogTitle>
              <DialogDescription>
                Contul tƒÉu este securizat prin Google
              </DialogDescription>
            </DialogHeader>
            <div className="space-y-4 pt-4">
              <div className="p-4 rounded-lg bg-primary/5 border border-primary/20">
                <div className="flex items-center gap-3 mb-3">
                  <svg className="h-8 w-8" viewBox="0 0 24 24">
                    <path
                      d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                      fill="#4285F4"
                    />
                    <path
                      d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                      fill="#34A853"
                    />
                    <path
                      d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                      fill="#FBBC05"
                    />
                    <path
                      d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                      fill="#EA4335"
                    />
                  </svg>
                  <div>
                    <p className="font-semibold text-foreground">Cont Google Conectat</p>
                    <p className="text-sm text-muted-foreground">{userEmail}</p>
                  </div>
                </div>
              </div>

              <div className="space-y-3">
                <div className="flex items-start gap-3 p-3 rounded-lg bg-success/10 border border-success/20">
                  <span className="text-lg">‚úÖ</span>
                  <p className="text-sm text-foreground">
                    <span className="font-semibold">ParolƒÉ securizatƒÉ</span> - GestionatƒÉ de Google
                  </p>
                </div>
                <div className="flex items-start gap-3 p-3 rounded-lg bg-success/10 border border-success/20">
                  <span className="text-lg">üîê</span>
                  <p className="text-sm text-foreground">
                    <span className="font-semibold">2FA activat</span> - DacƒÉ ai 2FA pe Google
                  </p>
                </div>
              </div>

              <div className="pt-2">
                <a 
                  href="https://myaccount.google.com/security" 
                  target="_blank" 
                  rel="noopener noreferrer"
                >
                  <Button variant="outline" className="w-full gap-2">
                    <ExternalLink className="h-4 w-4" />
                    GestioneazƒÉ securitatea √Æn Google
                  </Button>
                </a>
              </div>

              <p className="text-xs text-center text-muted-foreground">
                Pentru a schimba parola sau setƒÉrile de securitate, acceseazƒÉ contul tƒÉu Google.
              </p>
            </div>
          </DialogContent>
        </Dialog>
      </div>
    );
  }

  // For email/password users
  return (
    <div className="space-y-4">
      <Dialog>
        <DialogTrigger asChild>
          <PasswordInfoCard provider="email" />
        </DialogTrigger>
        <DialogContent>
          <DialogHeader>
            <DialogTitle className="flex items-center gap-2">
              <Lock className="h-5 w-5 text-primary" />
              SchimbƒÉ Parola
            </DialogTitle>
            <DialogDescription>
              Trimite un link de resetare a parolei pe email
            </DialogDescription>
          </DialogHeader>
          <div className="space-y-4 pt-4">
            <div className="p-4 rounded-lg bg-muted border">
              <div className="flex items-center gap-3">
                <Mail className="h-6 w-6 text-muted-foreground" />
                <div>
                  <p className="font-medium text-foreground">Email-ul tƒÉu</p>
                  <p className="text-sm text-muted-foreground">{userEmail}</p>
                </div>
              </div>
            </div>

            {emailSent ? (
              <div className="p-4 rounded-lg bg-success/10 border border-success/20 text-center">
                <span className="text-2xl">‚úÖ</span>
                <p className="font-medium text-foreground mt-2">Email trimis!</p>
                <p className="text-sm text-muted-foreground">
                  VerificƒÉ inbox-ul (»ôi folderul Spam) pentru linkul de resetare.
                </p>
              </div>
            ) : (
              <>
                <p className="text-sm text-muted-foreground">
                  Vom trimite un email cu un link securizat pentru a-»õi reseta parola.
                </p>
                
                <Button 
                  onClick={handlePasswordReset} 
                  disabled={isLoading}
                  className="w-full gap-2"
                >
                  {isLoading ? (
                    <>
                      <Loader2 className="h-4 w-4 animate-spin" />
                      Se trimite...
                    </>
                  ) : (
                    <>
                      <Mail className="h-4 w-4" />
                      Trimite link de resetare
                    </>
                  )}
                </Button>
              </>
            )}

            <p className="text-xs text-center text-muted-foreground">
              Linkul va expira √Æn 24 de ore.
            </p>
          </div>
        </DialogContent>
      </Dialog>
    </div>
  );
};
